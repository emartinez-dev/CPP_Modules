#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

void	ft_leaks(void)
{
	system("leaks unnecesary_violence");
}

int main()
{
	atexit(ft_leaks);
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return 0;
}